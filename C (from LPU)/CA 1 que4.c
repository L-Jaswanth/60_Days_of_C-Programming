#include<stdio.h>

int main ()
{
  signed int int1 = -32770;
  unsigned int int2 = -32770 ;
  signed char char1 = 255;
  unsigned char char2 = 255;

  printf("Signed int : %d\n", int1);
  printf("Unsigned int : %u\n", int2);
  printf ("Signed char : %d\n", char1);
  printf ("Unsigned char : %u\n", char2);
  return 0;
}
